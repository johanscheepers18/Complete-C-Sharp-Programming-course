using System;

namespace Variables
{ 
    
    class Program
    {
        public static void Main(String[] args)
        {
            //int is any whole number either positive or negative
            int intNumber1 = 33;
            int intNumber2 = -55;

            //float is any floating point number as should end with a 'f'
            float floatNumber1 = 812.33f;
            float floatNumber2 = -66.54f;

            double doubleNumber1 = 55.08;
            double doubleNumber2 = -78.32;

            //string could be either a single character, word or a sentence.
            //characters must be contained inside double quotation marks " ".
            string stringLetter = "a";
            string stringWord = "House";
            string stringSentence = "This is not 'Hello World'";

            //char is a single charater contained in single quotation marks ' '.
            char testChar = 'G';

            //bool is a value either true or false.
            bool testBool = true;
        }
    }
}
